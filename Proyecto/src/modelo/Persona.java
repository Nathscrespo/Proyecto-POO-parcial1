package modelo;

public class Persona {

    public String nombre;
    public String direccion;
    public double telefono;
    public String ciudad;
    public String email;

    public Persona(String n,String d,double t, String c,String e){
        nombre=n;
        direccion=d;
        telefono=t;
        ciudad=c;
        email=e;
    }
    
}
